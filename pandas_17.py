import pandas as pd

# concat()
d1 = pd.DataFrame({'ID':[1,2,3,4,5], 'class':['DSA', 'Web development', 'Mobile app development', 'AI/ML', 'GUI Development']})
print(d1)
print('----------------------------------------------------------------------------------------------')
d2 = pd.DataFrame({'ID':[1,2,3,4,5,6], 'credits':[120, 100, 110, 140, 130, 1000]})
print(d2)


print('----------------------------------------------------------------------------------------------')
print(pd.concat([d1,d2]))
print('----------------------------------------------------------------------------------------------')
print(pd.concat([d2,d1]))
print('----------------------------------------------------------------------------------------------')
print(pd.concat([d2,d1], axis=1)) # 0->row(default) 1->column
print('----------------------------------------------------------------------------------------------')
print(pd.concat([d2,d1], axis=0, ignore_index = True))
print('----------------------------------------------------------------------------------------------')
print(pd.concat([d2,d1], axis=1, join='inner')) #outer->union(default) inner->intersection
print('----------------------------------------------------------------------------------------------')
print(pd.concat([d2,d1], axis=1, join='outer'))
print('----------------------------------------------------------------------------------------------')
print(pd.concat([d2,d1], keys=['d2', 'd1']))
print('----------------------------------------------------------------------------------------------')
print(pd.concat([d2,d1], axis=1, keys=['d2', 'd1']))